// Helper function to get translated string
function t(key) {
  return (T[MATCH_LANG] && T[MATCH_LANG][key]) || T.en[key] || key;
}

// Apply translations to elements with data-t attribute
function applyTranslations() {
  document.querySelectorAll('[data-t]').forEach(function(el) {
    var key = el.getAttribute('data-t');
    var val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      if (el.getAttribute('data-t-attr') === 'placeholder') {
        el.placeholder = val;
      } else {
        el.value = val;
      }
    } else if (el.tagName === 'OPTION') {
      el.textContent = val;
    } else {
      el.innerHTML = val;
    }
  });
}

